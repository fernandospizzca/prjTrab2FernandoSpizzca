    private void btnPesqQuartoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqQuartoActionPerformed
        int x;
        for (x = 0; x < hotel.size(); x++) {
            if (hotel.get(x) instanceof QuartoHotel) {
                if (hotel.get(x).getNumQuarto() == Integer.parseInt(txtNumQuarto.getText())) {
                    break;
                }
            }
        }
        
        if (x < hotel.size()) {
            posQuarto = x;
        } else {
            posQuarto = -1;
        }
        if (posQuarto >= 0) {
            if (hotel.get(posQuarto).isSituacao() == false) {
                txtSituacao.setText(" Livre");
                txtDataEntrada.setEnabled(true);
                txtDataEntrada.setText(null);
                txtDataSaida.setText(null);
                txtTotalPagar.setText(null);
                txtDataEntrada.requestFocus();
                btnReservar.setEnabled(true);
            } else {
                txtSituacao.setText(" Reservado");
                JOptionPane.showMessageDialog(this, "Quarto Reservado favor escolher outro quarto");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Quarto não cadastrado, favor realizar o cadastro ou pesquisar outro");
        }
    }//GEN-LAST:event_btnPesqQuartoActionPerformed
    
    private int calcularDias(String txtDt1, String txtDt2) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Calendar data1 = Calendar.getInstance();
        Calendar data2 = Calendar.getInstance();
        int dias;
        try {
            data1.setTime(sdf.parse(txtDt1));
            data2.setTime(sdf.parse(txtDt2));
        } catch (java.text.ParseException e) {
        }
        dias = data2.get(Calendar.DAY_OF_YEAR) - data1.get(Calendar.DAY_OF_YEAR);
        return dias;
    }
        
    
