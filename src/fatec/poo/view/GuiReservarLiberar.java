    private void btnPesqRegFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqRegFuncActionPerformed
        int x;
        for (x = 0; x < cadastro.size(); x++) {
            if (cadastro.get(x) instanceof Atendente) {
                if (((Atendente) cadastro.get(x)).getRegFunc().equals(txtRegFunc.getText())) {
                    break;
                }
            }
        }
        if (x < cadastro.size()) {
            posAtendente = x;
        } else {
            posAtendente = -1;
        }

        if (posAtendente >= 0) {
            txtResultPesqFunc.setText(null);
            txtCpfHospede.setText(null);
            txtResultPesqHospede.setText(null);
            txtNumQuarto.setText(null);
            txtSituacao.setText(null);
            txtDataEntrada.setText(null);
            txtDataSaida.setText(null);
            txtTotalPagar.setText(null);
            txtResultPesqFunc.setText(cadastro.get(posAtendente).getNome());
            txtRegFunc.setEnabled(false);
            btnPesqRegFunc.setEnabled(false);
            btnPesqCpfHospede.setEnabled(true);
            txtCpfHospede.setEnabled(true);
            txtCpfHospede.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Atendente não cadastrado, favor realizar o cadastro do funcionário ou pesquisar outro");
        }
    }//GEN-LAST:event_btnPesqRegFuncActionPerformed

    private void btnPesqCpfHospedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqCpfHospedeActionPerformed
        int x;
        for (x = 0; x < cadastro.size(); x++) {
            if (cadastro.get(x) instanceof Hospede) {
                if (((Hospede) cadastro.get(x)).getCpf().equals(txtCpfHospede.getText())) {
                    break;
                }
            }
        }
        if (x < cadastro.size()) {
            posHospede = x;
        } else {
            posHospede = -1;
        }

        if (posHospede >= 0) {
            txtResultPesqHospede.setText(cadastro.get(posHospede).getNome());
            txtCpfHospede.setEnabled(false);
            btnPesqCpfHospede.setEnabled(false);
            if (((Hospede) cadastro.get(posHospede)).getQuartoHotel() != null) {
                txtNumQuarto.setText(Integer.toString(((Hospede) cadastro.get(posHospede)).getQuartoHotel().getNumQuarto()));
                if (((Hospede) cadastro.get(posHospede)).getQuartoHotel().isSituacao() == true) {
                    txtSituacao.setText(" Reservado");
                } else {
                    txtSituacao.setText(" Livre");
                }
                txtDataEntrada.setText((((Hospede) cadastro.get(posHospede)).getQuartoHotel().getDataEntrada()));
                txtDataSaida.setEnabled(true);
                btnReservar.setEnabled(false);
                btnLiberar.setEnabled(true);
                txtDataSaida.requestFocus();
            } else {
                txtCpfHospede.setEnabled(false);
                btnPesqCpfHospede.setEnabled(false);
                txtNumQuarto.setEnabled(true);
                btnPesqQuarto.setEnabled(true);
                txtNumQuarto.requestFocus();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Hópesde não cadastrado, favor realizar o cadastro ou pesquisar outro");
        }
    }//GEN-LAST:event_btnPesqCpfHospedeActionPerformed


    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        hotel.get(posQuarto).reservar((Hospede)cadastro.get(posHospede), (Atendente)cadastro.get(posAtendente));
        ((Atendente) cadastro.get(posAtendente)).addQuartoHotel(hotel.get(posQuarto));
        ((Hospede) cadastro.get(posHospede)).setQuartoHotel(hotel.get(posQuarto));
        
        hotel.get(posQuarto).setDataEntrada(txtDataEntrada.getText());
        
        txtRegFunc.setText(null);
        txtResultPesqFunc.setText(null);
        txtResultPesqHospede.setText(null);
        txtCpfHospede.setText(null);
        txtNumQuarto.setText(null);
        txtDataEntrada.setText(null);
        txtRegFunc.setEnabled(true);
        btnPesqRegFunc.setEnabled(true);
        txtCpfHospede.setEnabled(false);
        txtNumQuarto.setEnabled(false);
        btnPesqQuarto.setEnabled(false);
        txtDataEntrada.setEnabled(false);
        txtDataSaida.setEnabled(false);
        btnReservar.setEnabled(false);
        txtRegFunc.requestFocus();
    }//GEN-LAST:event_btnReservarActionPerformed

    private void btnLiberarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberarActionPerformed
            txtTotalPagar.setText(df.format(((Hospede) cadastro.get(posHospede)).getQuartoHotel()
                .liberar(calcularDias((((Hospede) cadastro.get(posHospede)).getQuartoHotel().getDataEntrada()),
                        txtDataSaida.getText()), ((Hospede) cadastro.get(posHospede)).getTxDesconto())));
            
            txtDataSaida.setEnabled(false);
            txtRegFunc.setEnabled(true);
            btnPesqRegFunc.setEnabled(true);
            txtRegFunc.requestFocus();
            
    }//GEN-LAST:event_btnLiberarActionPerformed

