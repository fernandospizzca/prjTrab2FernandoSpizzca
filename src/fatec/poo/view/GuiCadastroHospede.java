    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int x;
        for (x = 0; x < cadPessoas.size(); x++) {
            if (cadPessoas.get(x) instanceof Hospede) {
                if (((Hospede) cadPessoas.get(x)).getCpf().equals(txtCpf.getText())) {
                    break;
                }
            }
        }

        if (x < cadPessoas.size()) {
            posHospede = x;
        } else {
            posHospede = -1;
        }

        if (posHospede >= 0) {
            txtCpf.setText(((Hospede) cadPessoas.get(posHospede)).getCpf());
            txtNome.setText(cadPessoas.get(posHospede).getNome());
            txtEndereco.setText(cadPessoas.get(posHospede).getEndereco());
            txtTelefone.setText(cadPessoas.get(posHospede).getTelefone());
            txtTxDesconto.setText(Double.toString(((Hospede) cadPessoas.get(posHospede)).getTxDesconto()));
            if(((Hospede) cadPessoas.get(posHospede)).getSexo().equals("F")){
                rdbFeminino.setSelected(true);
            }
            else{
                rdbMasculino.setSelected(true);
            }

            btnConsultar.setEnabled(true);
            btnInserir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Hóspede não cadastrado, caso deseje cadastrar informe os dados a seguir e clique inserir.");
            txtNome.setText(null);
            txtEndereco.setText(null);
            txtTelefone.setText(null);
            txtTxDesconto.setText(null);
            btnConsultar.setEnabled(true);
            btnInserir.setEnabled(true);
            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            txtNome.requestFocus();
        }
        txtCpf.setEnabled(true);
        rdbFeminino.setEnabled(true);
        rdbMasculino.setEnabled(true);
        txtNome.setEnabled(true);
        txtEndereco.setEnabled(true);
        txtTelefone.setEnabled(true);
        txtTxDesconto.setEnabled(true);
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        p = new Hospede(txtNome.getText(), txtCpf.getText());

        p.setEndereco(txtEndereco.getText());
        p.setTelefone(txtTelefone.getText());
        ((Hospede) p).setTxDesconto(Double.parseDouble(txtTxDesconto.getText()));
        if(rdbFeminino.isSelected()){
            ((Hospede) p).setSexo("F");
        }
        else{
            ((Hospede) p).setSexo("M");
        }

        cadPessoas.add(p);

        txtCpf.setText(null);
        txtNome.setText(null);
        txtEndereco.setText(null);
        txtTelefone.setText(null);
        txtTxDesconto.setText(null);
        btnConsultar.setEnabled(true);
        btnInserir.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        txtCpf.setEnabled(true);
        rdbFeminino.setEnabled(false);
        rdbMasculino.setEnabled(false);
        txtNome.setEnabled(false);
        txtEndereco.setEnabled(false);
        txtTelefone.setEnabled(false);
        txtTxDesconto.setEnabled(false);
        rdbFeminino.setSelected(true);
        txtCpf.requestFocus();
    }//GEN-LAST:event_btnInserirActionPerformed

